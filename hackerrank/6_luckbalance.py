def luckBalance(k, contests):
    ans=0
    contest=[]
    for (i,j) in contests:
        if j:
            contest.append(i)
        else:
            ans+=i
    contest.sort(reverse=True)
    ans+=sum(contest[:k])
    ans-=sum(contest[k:])
    return ans
            
